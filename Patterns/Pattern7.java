class Pattern7 
{
	public static void main(String[] args) 
	{
		int n=10;
		for (int i =1; i<=5;i++ )
		{
			for (int j=1;j<=5 ;j++ )
			{
				n=n+1;
				System.out.print(" "+n+" ");
			}
			System.out.println();
		}
		
	}
}


