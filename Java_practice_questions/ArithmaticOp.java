class ArithmaticOp 
{
	public static void main(String[] args) 
	{

		// operations
		System.out.println(4+3);
		System.out.println(8-6);
		System.out.println(13*10);
		System.out.println(47/5);
		System.out.println(47.0/5);
		System.out.println(47.0/5.0);
		System.out.println(47/5.0);
		System.out.println(47%5);

		// concatination
		double bh = 5.8;
		System.out.println("Name : " +bh+ "feet");
		System.out.println("Pramod : " +bh+ "feet");
	}
}
